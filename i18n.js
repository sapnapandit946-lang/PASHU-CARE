// Language Changer Logic
function changeLanguage(langCode) {
    if (!TRANSLATIONS[langCode]) {
        langCode = 'en';
    }

    document.documentElement.lang = langCode;
    localStorage.setItem('pashucare_lang', langCode);
    const selectedTable = TRANSLATIONS[langCode] || {};
    const fallbackTable = TRANSLATIONS.en || {};

    // Find all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translated = selectedTable[key] || fallbackTable[key];
        if (translated) {
            el.innerHTML = translated;
        }
    });

    // Handle standard placeholders (input elements)
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const translated = selectedTable[key] || fallbackTable[key];
        if (translated) {
            el.setAttribute('placeholder', translated);
        }
    });

    // Update active state in settings dropdown if exists
    const langSelect = document.getElementById('langSelect');
    if (langSelect) langSelect.value = langCode;
    const langSelectPanel = document.getElementById('langSelectPanel');
    if (langSelectPanel) langSelectPanel.value = langCode;
    const langSelectSettings = document.getElementById('langSelectSettings');
    if (langSelectSettings) langSelectSettings.value = langCode;
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('pashucare_lang') || 'en';
    changeLanguage(savedLang);
});
